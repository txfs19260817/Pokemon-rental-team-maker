// Check length of pokemon list
func CheckPokemonListValid(pokemonList *[]Pokemon) (bool, error) {
	if len(*pokemonList) <= 0 || len(*pokemonList) > 6 {
		return false, errors.New("Length of `pokemonList` must between 1 and 6. ")
	}

	return true, nil
}

// Check the number of types
func CheckTypesValid(types *[]string) (bool, error) {
	if len(*types) != 1 && len(*types) != 2 {
		return false, errors.New("The number of types must between 1 and 2. ")
	}

	return true, nil
}

// Check the number of moves
func CheckMovesListValid(moves *[]string) (bool, error) {
	if len(*moves) <= 0 || len(*moves) > 4 {
		return false, errors.New("The number of moves must between 1 and 4. ")
	}

	return true, nil
}

// Check slot number
func CheckSlotNumber(slot int) (bool, error) {
	if slot < 0 || slot > 6 {
		return false, errors.New("Slot must between 1 and 6. ")
	}

	return true, nil
}

// Purge all non-alphabet-numerical runes and lower the string.
func String2Filename(s string) string {
	reg := regexp.MustCompile("[^a-zA-Z0-9]+")
	return strings.ToLower(reg.ReplaceAllString(s, "${1}"))
}
